/** 선택 텍스트에 대한 한자 후보. 전체 단어 우선, 없으면 마지막 음절. */
export function hanjaCandidates(text: string): string[] {
  const t = text.trim()
  if (!t) return []
  if (HANJA_DICT[t]) return HANJA_DICT[t]
  const last = t.slice(-1)
  return HANJA_DICT[last] ?? []
}
